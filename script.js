function borderBottom(id, style) {
    document.getElementById(id).style.borderBottom = style;
}
function border(id, style) {
    document.getElementById(id).style.border = style;
}
function fontColor(id, color) {
    document.getElementById(id).style.color = color;
}
function background_color(id, color) {
    document.getElementById(id).style.backgroundColor = color;
}
function doDisplay(id, display) {
    var con = document.getElementById(id);
    con.style.display = display;
}